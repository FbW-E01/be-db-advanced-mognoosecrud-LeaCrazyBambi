import Mongoose from "mongoose";

const userSchema = new Mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 8,
  },
  role: {
    type: String,
    default: "User",
  },
});

export const User = Mongoose.model("userz", userSchema);
